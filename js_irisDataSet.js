async function main(){
  const [trainX, trainY, testX, testY] = processData();
    
  const model = await buildModel(trainX, trainY, testX, testY); 

  makeAPrediction(model, testX, testY);
}



async function makeAPrediction(model, testX, testY){

  tf.tidy(() => {

      const prediction = model.predict(testX);

      console.log("Actual Label"); 
      testY.print(); 

      console.log("prediction"); 
      prediction.print(); 

  });

}



async function buildModel(trainX, trainY, testX, testY){
  const model = tf.sequential(); 

  model.add(tf.layers.dense({
      units: 10, 
      activation: "sigmoid", 
      inputShape: [trainX.shape[1]]
  }));

  model.add(tf.layers.dense({
      units: 3, 
      activation: "softmax" 
  }));

  model.compile({
      optimizer: tf.train.adam(0.01), 
      loss: "categoricalCrossentropy",
      metrics: ["accuracy"]
  });

  await model.fit(trainX, trainY, {
      epochs: 50, 
      validationData: [testX, testY], 
      callbacks: {
          onEpochEnd: async (epoch, logs) => {
              console.log("Epoch " + epoch); 
              console.log(logs.acc);
          }
      }
  });

  return model; 
}


function processData(){
  const dataByClass   = []; 
  const labelsByClass = []; 


  return tf.tidy(() => {
      for(let i = 0; i < IRIS_CLASSES.length; ++i){
          dataByClass.push([]);
          labelsByClass.push([]);
      }

      for(const sample of IRIS_DATA){
          const label = sample[sample.length -1]; 

          const features = sample.slice(0, sample.length - 1);

          labelsByClass[label].push(label);

          dataByClass[label].push(features);
      }


      const trainXDataset = []; 
      const trainYDataset = [];
      const testXDataset  = []; 
      const testYDataset  = [];

      for(let i = 0; i < IRIS_CLASSES.length; ++i){

          const [trainX, trainY, testX, testY] = convertDataToTensors(dataByClass[i], labelsByClass[i]); 

          trainXDataset.push(trainX); 
          testXDataset.push(testX);
          trainYDataset.push(trainY);
          testYDataset.push(testY);

  
      }

      const axis = 0; 

      

      return [tf.concat(trainXDataset, axis), 
             tf.concat(trainYDataset, axis), 
             tf.concat(testXDataset, axis), 
             tf.concat(testYDataset, axis)] 
      
  });
}

function convertDataToTensors(x, y){

  if(x.length != y.length){
      throw new Error("x and y are different lengths");
  }

  const numberOfDimensions = x[0].length;
  const Xtensor = tf.tensor2d(x, [x.length, numberOfDimensions]);
  const Ytensor = tf.oneHot(tf.cast(tf.tensor1d(y), "int32"), IRIS_CLASSES.length); 

  let split = 0.2; 

  const totalnNumberOfSamples   = x.length;
  const numberOfTestingSamples  = totalnNumberOfSamples * split; 
  const numberOfTrainingSamples = totalnNumberOfSamples - numberOfTestingSamples;
  const trainX = Xtensor.slice([0,0], [numberOfTrainingSamples, numberOfDimensions]);
  const testX  = Xtensor.slice([numberOfTrainingSamples, 0], [numberOfTestingSamples, numberOfDimensions]);
  const trainY = Ytensor.slice([0,0], [numberOfTrainingSamples, IRIS_CLASSES.length]);
  const testY  = Ytensor.slice([0,0], [numberOfTestingSamples, IRIS_CLASSES.length]);

  return [trainX, trainY, testX, testY];
}


